namespace _02.CompareSimpleMaths
{
    internal class SimpleMathsOperations
    {
        internal static dynamic Sum(dynamic firstNumber, dynamic secondNumber)
        {
            var sum = firstNumber + secondNumber;

            return sum;
        }

        internal static dynamic Substract(dynamic firstNumber, dynamic secondNumber)
        {
            var sum = firstNumber - secondNumber;

            return sum;
        }

        internal static dynamic Multiply(dynamic firstNumber, dynamic secondNumber)
        {
            var sum = firstNumber * secondNumber;

            return sum;
        }

        internal static dynamic Divide(dynamic firstNumber, dynamic secondNumber)
        {
            var sum = firstNumber / secondNumber;

            return sum;
        }
    }
}
