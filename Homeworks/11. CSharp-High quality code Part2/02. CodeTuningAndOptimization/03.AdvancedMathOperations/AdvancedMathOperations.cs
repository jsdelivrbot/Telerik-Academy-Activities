using System;

namespace _03.AdvancedMathOperations
{
    internal class AdvancedMathOperations
    {
        internal static dynamic SquareRoot(dynamic number)
        {
            var sum = Math.Sqrt(number);

            return sum;
        }

        internal static dynamic NaturalLogarithm(dynamic number)
        {
            var sum = Math.Log(number);

            return sum;
        }

        internal static dynamic Sinus(dynamic number)
        {
            var sum = Math.Sin(number);

            return sum;
        }
    }
}
